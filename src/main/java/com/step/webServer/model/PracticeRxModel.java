package com.step.webServer.model;

public class PracticeRxModel {
    private String practicerxId;
    private Boolean checked;
    private String note;

    public String getPracticerxId() {
        return practicerxId;
    }

    public void setPracticerxId(String practicerxId) {
        this.practicerxId = practicerxId;
    }

    public Boolean getChecked() {
        return checked;
    }

    public void setChecked(Boolean checked) {
        this.checked = checked;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
